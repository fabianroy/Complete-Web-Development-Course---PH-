// While Loop 

let num = 1;
while (num <= 10) {
  console.log(num);
  num++;
}

let numA = 1;
let sum = 0;
while (numA <= 10){
  sum = sum + numA
  console.log(sum);
  numA++;
}

// Even Number 

let numB = 1;
while (numB <= 10){
  console.log(numB);
  if (numB % 2 == 0){
    console.log('Even =', numB);
  }
  numB++;
}
