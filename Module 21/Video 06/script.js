// ***************** MATH : Min & Max *****************
console.log(Math.min(45, -99, 2, -55, 0, -4, 33));  // -99
console.log(Math.max(45, -99, 2, -55, 0, -4, 33));  // 45

// ***************** MATH : PI *****************
console.log(Math.PI); // 3.141592653589793

// ***************** MATH : abs *****************
console.log(Math.abs(10 - 15)); // Gives the absolute value of the number (** GAP **) (ans : 5)

// ***************** MATH : Round *****************
console.log(Math.round(5.6)); // Rounds the number to the nearest integer (ans : 6)

// ***************** MATH : Floor *****************
console.log(Math.floor(5.6)); // Rounds the number down to the nearest integer (ans : 5)

// ***************** MATH : Ceil *****************
console.log(Math.ceil(5.6)); // Rounds the number up to the nearest integer (ans : 6)

// ***************** MATH : Random *****************
console.log(Math.random()*10);
console.log(Math.random()*10);
const result = Math.round(Math.random()*10);
console.log(result);
